using UnityEngine;
using UnityEngine.SceneManagement;
using UnityEngine.UI;




public class PauseMenuPanel : PanelWithButton       //挂载在Canvas游戏物体上，因为整个游戏过程中都有可能会用到此界面
{
    public Button ResumeButton;
    public Button MainMenuButton;
    public Button QuitButton;


    //加static就无需获得此脚本的引用即可调用此变量
    public static bool IsGamePaused {  get; private set; }







    protected override void Awake()
    {
        base.Awake();

        //检查按钮组件是否存在
        if (ResumeButton == null || MainMenuButton == null || QuitButton == null)
        {
            Debug.LogError("Some buttons are not assigned in the PauseMenuPanel.");
            return;
        }


        //将按钮和函数绑定起来
        ResumeButton.onClick.AddListener(() => Resume());
        MainMenuButton.onClick.AddListener(() => BackToMainMenu());
        QuitButton.onClick.AddListener(() => QuitGame());


        //默认按钮为“恢复”按钮
        firstSelectedButton = ResumeButton.gameObject;
    }


    private void Start()
    {
        //设置当前界面的名字
        panelName = UIManager.Instance.UIKeys.PauseMenuPanel;

        //初始化布尔
        IsGamePaused = false;

        //设置此界面的淡入值
        FadeInAlpha = 0.75f;
    }


    protected override void Update()
    {
        //当界面打开时才调用父类的Update函数
        if (CanvasGroup.alpha == FadeInAlpha)
        {
            base.Update();

        }


        //持续检查玩家是否按下Esc，在暂停过程中按下的话则恢复游戏，否则就暂停游戏
        if (Input.GetKeyDown(KeyCode.Escape))
        {
            //游戏暂停时
            if (IsGamePaused)
            {
                Resume();
            }

            //游戏正常时
            else
            {
                Pause();
            }
        }                  
    }





    //恢复游戏
    private void Resume()
    {
        base.OnDisable();

        //关闭界面
        Fade(CanvasGroup, FadeOutAlpha, 0, false);

        //恢复游戏的时间流逝
        Time.timeScale = 1f;
        
        IsGamePaused = false;
    }


    //暂停游戏
    private void Pause()
    {
        base.OnEnable();        //Bug：在其他界面打开时，打开此界面后无法将其加入父类中的列表

        //打开界面
        Fade(CanvasGroup, FadeInAlpha, 0, true);

        //暂停游戏的时间流逝
        Time.timeScale = 0f;        

        IsGamePaused = true;
    }


    //返回主菜单
    private void BackToMainMenu()
    {
        //先恢复游戏，随后返回主菜单
        Resume();

        //返回主界面场景
        SceneManager.LoadScene("MainMenu");

        //ClosePanel();
    }


    //退出游戏
    private void QuitGame()
    {
        //退出游戏（用于本地游戏包）
        Application.Quit();

        //在Unity编辑器内退出游玩模式
        #if UNITY_EDITOR
        UnityEditor.EditorApplication.isPlaying = false;
        #endif
    }
}